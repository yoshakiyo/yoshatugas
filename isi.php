<h2>PROFIL BIDANG PERIKANAN TANGKAP</h2><br/>

Bidang Perikanan Tangkap mempunyai tugas pokok penyiapan perumusan kebijakan teknis,<br/> pembinaan dan pelaksanaan di bidang prasarana perikanan tangkap,<br/> sarana perikanan tangkap dan pengawasan dan pengelolaan sumberdaya ikan.

Untuk menyelenggarakan tugas pokok dimaksud, Bidang Perikanan Tangkap mempunyai fungsi :<br/>

    Penyiapan bahan perumusan kebijakan teknis, pembinaan dan pelaksanaan di bidang prasarana perikanan tangkap;<br/>
    Penyiapan bahan perumusan kebijakan teknis, pembinaan dan pelaksanaan di bidang sarana perikanan tangkap;<br/>
    Penyiapan bahan perumusan kebijakan teknis, pembinaan dan pelaksanaan di bidang pengawasan dan pengelolaan sumberdaya ikan;<br/>
    Pelaksanaan tugas lain yang diberikan oleh Kepala Dinas sesuai dengan tugas dan fungsinya.<br/>

<h2>Bidang Perikanan Tangkap membawahi :</h2>

    Seksi Prasarana Perikanan Tangkap<br/>
    Seksi Sarana Perikanan Tangkap<br/>
    Seksi Pengawasan dan Pengelolaan Sumber daya Ikan<br/><br/>

<h2>Nelayan</h2>

Nelayan merupakan orang yang secara aktif melakukan pekerjaan dalam operasi penangkapan ikan/binatang air/tanaman air.<br/> Selama 5 tahun terakhir jumlah nelayan jawa Tengah mengalami fluktuasi sedangkan jumlah armada stabil.<br/>

Jumlah nelayan perairan umum mengalami kenaikan yang diikuti naiknnya jumlah armada yang digunakan oleh nelayan.<br/> Nelayan perairan umum banyak menggunakan perahu tanpa motor hanya sebagian kecil yang menggunakan motor tempel.<br/>

Armada Penangkapan dan Alat Tangkap<br/>

Armada penangkapan Jawa Tengah didominasi oleh motor tempel pada penangkapan laut.<br/> Sedangkan penangkapan di perairan umum didominasi oleh perahu tanpa motor. <br/>Pada tahun 2011, pukat kantong dan jaring insang didominasi alat tangkap yang digunakan oleh nelayan perikanan laut,<br/> berturut sekitar 43,87% dan 33, 35%. Di Perairan umum,<br/> mayoritas alat tangkap yang digunakan adalah jaring insang sekitar 27,21% dan pancing sekitar 21,18%.
